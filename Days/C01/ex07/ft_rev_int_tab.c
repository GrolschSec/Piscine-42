void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	d;
	int	temp;

	i = 0;
	d = size - 1;
	while (i < d)
	{
		temp = tab[i];
		tab[i] = tab[d];
		tab[d] = temp;
		i++;
		d--;
	}
}
